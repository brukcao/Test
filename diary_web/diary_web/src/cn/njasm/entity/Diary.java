package cn.njasm.entity;

public class Diary {
		private int d_id;
		private String d_name;
		private String d_content;
		private String d_dt;
		private int d_pri;
		private int u_id;
		public int getD_id() {
			return d_id;
		}
		public void setD_id(int d_id) {
			this.d_id = d_id;
		}
		public String getD_name() {
			return d_name;
		}
		public void setD_name(String d_name) {
			this.d_name = d_name;
		}
		public String getD_content() {
			return d_content;
		}
		public void setD_content(String d_content) {
			this.d_content = d_content;
		}
		public String getD_dt() {
			return d_dt;
		}
		public void setD_dt(String d_dt) {
			this.d_dt = d_dt;
		}
		public int getD_pri() {
			return d_pri;
		}
		public void setD_pri(int d_pri) {
			this.d_pri = d_pri;
		}
		public int getU_id() {
			return u_id;
		}
		public void setU_id(int u_id) {
			this.u_id = u_id;
		}
		
}
